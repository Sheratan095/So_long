#include "libft.h"

void	*ft_memchr(const void *str, int c, size_t n)
{
	unsigned char	*res;
	unsigned int	i;

	i = 0;
	res = (unsigned char *)str;
	while (i < n)
	{
		if (res[i] == (unsigned char)c)
		{
			return (res + i);
		}
		i++;
	}
	return (NULL);
}
